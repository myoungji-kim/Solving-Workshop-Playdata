SELECT DEPARTMENT_NAME 학과이름, CAPACITY 현재정원, 
CASE WHEN CAPACITY >= 40 THEN '대강의실' 
        WHEN CAPACITY >= 30 THEN '중강의실' 
        ELSE '소강의실' END AS 강의실크기
FROM TB_DEPARTMENT
WHERE CATEGORY = '예체능';

SELECT *
FROM TB_DEPARTMENT
WHERE CAPACITY >= 20;


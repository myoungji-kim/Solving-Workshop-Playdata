SELECT RPAD(SUBSTR(STUDENT_SSN, 1, 8), 14, '*') 주민번호, STUDENT_NAME 이름
FROM TB_STUDENT
WHERE SUBSTR(STUDENT_SSN, 1, 2) = '80' AND STUDENT_NAME LIKE '김%' AND SUBSTR(STUDENT_SSN, 8, 1) = '2';


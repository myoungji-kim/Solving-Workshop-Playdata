SELECT DEPARTMENT_NO, STUDENT_NAME, COACH_PROFESSOR_NO, TO_CHAR(ENTRANCE_DATE, 'YYYY')
FROM TB_STUDENT
WHERE STUDENT_ADDRESS IS NULL AND (TO_CHAR(ENTRANCE_DATE, 'YY')='06' OR TO_CHAR(ENTRANCE_DATE, 'YY')='05') 
ORDER BY ENTRANCE_DATE;
